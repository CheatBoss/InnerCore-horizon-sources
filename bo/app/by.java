package bo.app;

import java.util.*;

public class by implements cc
{
    private final Set<ca> a;
    
    public by(final Set<ca> a) {
        this.a = a;
    }
    
    public Set<ca> a() {
        return this.a;
    }
    
    @Override
    public boolean b() {
        final Set<ca> a = this.a;
        return a != null && a.isEmpty();
    }
}
